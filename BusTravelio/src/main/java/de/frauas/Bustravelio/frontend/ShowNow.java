        public void showPayment(){
         ArrayList<PaymentList> list = tt.PaymentList(username);
          model = (DefaultTableModel)jTable1.getModel();
         Object [] row = new Object[7];
            for(int i =0; i<list.size();i++){
                row[0]=list.get(i).getId();
                row[1]=list.get(i).getsentOrReceived();
                row[2]=list.get(i).getsentTo();
                row[3]=list.get(i).getreseivedFrom();
                row[4]=list.get(i).getprice();
                row[5]=list.get(i).getdate1();
                row[6]=list.get(i).gettime1();

                model.addRow(row);
            }
        }
